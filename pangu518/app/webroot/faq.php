              <p class="text"><strong>14、如果我获得了当期分红，是如何支付的呢？ </strong><br>
                答：对每一位获得当期分红的会员，我们都会在次日将分红金额转入个人会员邮政储蓄银行卡号上，（如遇国家法定节假日则顺延）。 <br>
  <br>

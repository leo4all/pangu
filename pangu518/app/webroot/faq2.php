            <td><p><strong>1</strong><strong>、怎样才能成为你们的会员消费单位呢？                  <br>
              </strong>　答：您可以登陆我们盘古消费财富网站<a href="http://www.1n1000j.com/">www.pangu518.com</a>在线申请，或者拨打当地盘古服务工作站的电话，就会有专人上门为您服务。<br>
              <strong>2</strong><strong>、签约成为你们的会员消费单位有哪些条件和要求呢？<br>
              </strong>　答：只要您是合法、诚信的个体工商户或独立法人单位，具有完全民事权利和行为能力，能够独立承担民事责任，具备当地工商行政管理部门核准发放的有效工商营业执照，并充分了解我们“消费资本财富分红体系”的各项规定，并自愿遵照执行的，都可以申请成为我们公司的会员消费单位。<strong> </strong><br>
              <strong>3</strong><strong>、签约成为你们的会员消费单位有哪些好处？能给我带来多大利益呢？<br>
              </strong>　答：我们的个人会员是所有普通的消费者，作为消费者每天都会产生消费，由于成为我们的个人会员完全免费，没有任何的条件限制，而且还可以通过日常的消费获得财富，所以我们的个人会员将是一个巨大的消费群体，我们会通过各种形式的广告宣传让个人会员到我们的会员消费单位去消费。届时，到盘古会员消费单位消费必将会形成一种新的消费时尚，我们会在公司网站定期推出会员消费单位的广告，进行介绍宣传。对商家来说，这样不仅能通过我们的广告宣传增加商家的知名度和美誉度，而且形成一批长期稳定的客户群，这对您来说必然会增加收入。  同时，举办各种旨在为会员消费单位的知名度和销售业绩的提升以及利用我们的专业团队帮助会员消费单位进行资源整合、市场战略和企业文化的策划和建议。 <br>
              <strong>4. </strong><strong>会员消费单位如何享受消费资本财富体系的分红所带来的财富呢？</strong><br>
              答：会员消费单位有3种方式获得消费财富，而且可以长期享受： <br>
              （1）会员消费单位可以参与公司分红，每购买盘古消费资本财富分红资格凭证25张（共计50个财富积点）即有一次分红资格，购买盘古消费资本财富分红资格凭证50张（共计100个财富积点）即有两次分红资格，依次类推。（详情请查看《盘古消费资本财富分红规则》） <br>
              （2）会员消费单位可以成为公司幸运分红小组成员，公司在每期分红前的一周由电脑随机抽出一名幸运会员消费单位，由该单位选派一名代表参加公司的分红小组，其所产生的差旅费用(即幸运会员从家出发到公司的往返费用，按实际发生额)到达公司后按票据予以报销，另外再发给4999元（含税）的劳务补贴。 <br>
              （3）会员消费单位可以成为公司幸运会员消费单位，公司每月末在每个省的会员消费单位中由电脑随机抽出10家幸运会员消费单位，每个幸运会员消费单位将获奖金4000元（含税），并于次日将此奖金转入该会员消费单位代表的邮政储蓄银行卡上。（如遇国家法定节假日则顺延）<br>
  <strong>5. </strong><strong>签约成为会员消费单位需要办理什么手续？要交纳什么费用吗？</strong><br>
              答：在签约成为会员消费单位的时侯，您所提交的手续包括营业执照复印件、联系人资料和电话传真以及银行帐号等信息，我们会与您签署一份合作协议，约定返还消费资格凭证的比率，并为您授牌。（详情请参考会员消费单位权责利和阅读会员消费单位合作协议书）<strong>　　</strong> <br>
  <strong>6. </strong><strong>签约成为会员消费单位必须要购买消费资格凭证吗？每次购买的基数是多少呢？</strong><br>
              答：消费分红资格凭证是我们的个人会员在您那里消费后根据我们之间所签署的协议您应该返还给个人会员的分红资格凭证。如果您没有购买我们的消费分红资格凭证，当我们通过广告宣传，大量的个人会员来到您那里购物消费时向您索取消费分红资格凭证时，您拿什么返还给个人会员呢？所以，购买消费分红资格凭证不仅是个人会员获得财富的象征，更是会员消费单位赢得市场竞争和获得更多利润和消费财富的手段。我们的消费分红资格凭证每份2个财富积点，每次购买的基数是50份，也就是说，每次购买的基数是100个财富积点，当然，您可以根据您的营业状况准备充足，以便您的经营。<strong>　　 </strong><br>
  <strong>7. </strong><strong>我购买了消费分红资格凭证，相当于把我一定比例的利润拿出来分享了，这样我的利润率下降了，对我来说，有什么好处呢？</strong><br>
              答：首先，我们在签署合作协议时，您应该已经考虑了您所经营产品或服务的利润率，根据您的意愿，然后我们才会约定一个消费分红资格凭证的返还比率；其次，事实上我们通过我们的运营平台和操作方式以及我们的影响力，在通过媒体广告宣传等手段将您的经营活动进行广告宣传，相当于通过我们专业的模式在帮助您做公关和广告，为您带来源源不断的客流量，并形成长期稳定忠实的客户群体，您所付出的仅仅是非常小的比例就能带来长期的收益；再则，我们的消费财富分红体系，使您也能享受消费资本所带来的分红财富，你所购买和返还个人会员的消费分红资格凭证越多，您所享受的消费分红资格就越多。<strong> </strong><br>
  <strong>8. </strong><strong>消费分红资格凭证遗失怎么办？</strong><br>
              答：如您不慎将未使用过的消费分红资格凭证遗失，一定要及时与我们的客户服务中心取得联系，并且说明丢失时间、地点、原因、消费分红资格凭证序号及贵单位名称，填写《消费分红资格凭证遗失证明》，负责人签字并加盖公章。我们接到您的遗失声明后，会马上处理。由于消费分红资格凭证丢失所带来的损失由贵单位自行承担。<br>
  <strong>9. </strong><strong>消费分红资格凭证有有效期吗？如果长时间没有使用会不会作废呢？</strong><br>
              答：消费分红资格凭证没有有效期，只要没有刮开凭证上面的密码编号，都长期有效，不会作废。 <br>
  <strong>10. </strong><strong>消费分红资格凭证使用完了怎么购买呢？</strong><br>
              答：当您的消费分红资格凭证使用完了后，请到当地工作站购买。为了更好的为个人会员服务，建议您提前准备充足的消费分红资格凭证。 <br>
  <strong>11. </strong><strong>我怎样知道我是否获得了幸运会员消费单位，如果我无法前来，该怎么办呢？怎样参加分红小组抽取分红号码的工作呢？所产生的费用怎么算呢？</strong><br>
              答：当贵单位被系统随机抽取成为幸运会员消费单位时，我们的工作人员会在第一时间内通过电话与贵单位联系人联系，您可以安排好自己的时间和人员来我公司。如果您实在来不了，那很遗憾，我们会在当期另外抽取其他会员消费单位前来。单位选派一名代表来参加公司的分红小组的一切费用(即幸运消费单位从家出发到公司的往返费用，按实际发生额)到达公司后按票据予以报销，另外再发给4999元（含税）的劳务补贴。<br>
  <strong>12. </strong><strong>购买了消费分红资格凭证，如何参与消费财富分红？分红的金额是多少呢？</strong><br>
              答：会员消费单位参与消费财富分红的，如一次在工作站购本公司盘古消费资本财富分红资格凭证25张（共计50个财富积点），即有一次分红资格；如一次在工作站购本公司盘古消费资本财富分红资格凭证50张（共计100个财富积点），即有2次分红资格，依次类推。当期编写的号码和当期开出的分红号码相同，即可享受当期分红，每期分红金额最高为4999元（含税）。 <br>
  <strong>13. </strong><strong>我怎样知道获得了当期消费分红，怎样领取分红所得呢？领取分红所得有无时间限制和有效期呢？</strong><br>
              答：当期分红号码开出后，我们会在公司网站上进行公示。若您所编写的号码与开出的号码相同，则可享受当期分红。我们会在次日将分红所得转入该会员消费单位代表的邮政储蓄银行卡上（如遇国家法定节假日则顺延），时间及有效期无限制。 <br>
  <strong>14. </strong><strong>我单位没有邮政储蓄银行的卡？其它银行卡都不行吗？</strong>　 <br>
              答：因为邮政储蓄银行的网点遍布城乡，考虑到大多数会员的方便，若您无邮政储蓄卡，请申办一张邮政储蓄银行卡。目前暂不支持对其它银行卡的服务。 <br>
  <strong>15.</strong><strong>成为幸运会员消费单位有什么条件吗？幸运会员消费单位是如何抽取的？有什么好处呢？</strong><br>
              答：只要成为本公司的会员消费单位就有资格。幸运会员消费单位的抽取是由电脑系统随机抽取的，它有2种形式的幸运会员消费单位：1.系统在每期分红前随机抽取一个会员消费单位参与分红小组； 2. 公司每月末在每个省的会员消费单位中抽出10家幸运会员消费单位，每个幸运会员消费单位将获奖金4000元（含税），并于次日将此奖金转入该会员消费单位代表的邮政储蓄银行卡上    （国家法定节假日顺延）。<strong> </strong><br>
  <strong>16.</strong><strong>我单位如果不想继续购买消费分红资格凭证，欲终止或退出呢？ </strong><br>
  <strong>答：</strong>合作协议期满后贵单位如欲退出盘古消费财富分红体系，协议自动终止。在协议期间，会员消费单位欲终止协议，应提前15个工作日提前告知我公司，经我公司调查核实该会员消费单位无任何违反协议行为的，其所购盘古消费资本财富分红资格凭证未用完或会员消费单位名下可用金额未参与公司分红，经公司财务部核准，按80%退还（注：因为当您在购买分红凭证输入公司数据库时，市场业务人员的业绩提成10%，工作站佣金8%，银行汇费2%，共计20%的费用公司已经付出）如会员消费单位在协议期间出现违约行为，我公司有权要求贵单位退出盘古消费财富分红体系并在我公司网站及相关媒体公布，取消其在盘古消费财富分红体系中的所有权益,同时保留追究其违反协议所带来的经济责任和相关的法律责任。<strong> </strong><br>
  <strong>17.</strong><strong>会员消费单位若搬迁到别的城市或开办分支机构，可否保留会员消费单位身份？</strong><br>
              答 ：可以。我们的盘古服务工作站遍布全国各地，若贵单位搬迁到别的城市或开办分支机构，可向原所在地的盘古服务工作站申请。办理协议的迁移手续完毕，可在新搬迁或开办分支机构的城市重新签署协议，成为会员消费单位。<br>
  <strong>18.</strong><strong>我单位资料发生变化，可以修改吗？怎样修改呢？ </strong><br>
  <strong>　</strong>答：会员消费单位资料需要进行变更时，应通知我公司市场部人员，并将所变更的资料以书面形式加盖公章传真或发电子邮件给我们，我们将视该单位的变更资料为正式充分授权行为（如联系人资料等），变更后的资料重新录入系统。　 <br>
  <strong>19</strong><strong>、我单位可否一次性购买大金额的消费分红资格凭证？可否直接向其它人出售呢？</strong><br>
              答：我们的消费分红资格凭证每次购买基数是100个财富积点。当然，贵单位可以根据您的具体经营情况选择购买尽可能充足的消费分红资格凭证，以便您在会员购物和消费量大时有足够的消费分红资格凭证返还给个人会员，不至于在个人会员来到您处购物和消费时因为没有消费分红资格凭证返还的尴尬和不必要的矛盾。 </p>
            我们的消费分红资格凭证仅仅是个人会员在购物后所得到的一种消费财富分红资格，一方面是为了更好更多的为会员消费单位吸引和形成稳定的客户群体，另一方面也是消费资本变为财富的具体体现，按照我们所签署的协议要求，直接出售消费分红资格凭证是违反协议要求和法律法规的，所以我们是严格禁止将消费分红资格凭证直接出售的。<br>
            <strong>20</strong><strong>、我单位经营的种类和方式发生了变化，原来所约定的返还比率可以在协商重新制定吗？</strong><br>
　答：当然可以。贵单位在经营状况发生变化时，应及时通知我公司市场部工作人员，然后重新商定返还比率并形成协议的补充条款就可以了。<p class="text">&nbsp;</p></td>
